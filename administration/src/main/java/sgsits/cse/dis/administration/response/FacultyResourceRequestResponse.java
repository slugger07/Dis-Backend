package sgsits.cse.dis.administration.response;

public class FacultyResourceRequestResponse {

	public long getCreatedById() {
		return createdById;
	}
	public void setCreatedById(long createdById) {
		this.createdById = createdById;
	}
	public String getCreatedByName() {
		return createdByName;
	}
	public void setCreatedByName(String createdByName) {
		this.createdByName = createdByName;
	}
	public String getCreatedByDesignation() {
		return createdByDesignation;
	}
	public void setCreatedByDesignation(String createdByDesignation) {
		this.createdByDesignation = createdByDesignation;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getResourceCategory() {
		return resourceCategory;
	}
	public void setResourceCategory(String resource) {
		this.resourceCategory = resource;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDateOfResolution() {
		return dateOfResolution;
	}
	public void setDateOfResolution(String dateOfResolution) {
		this.dateOfResolution = dateOfResolution;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	public String getDeadlineOfResolution() {
		return deadlineOfResolution;
	}
	public void setDeadlineOfResolution(String deadlineOfResolution) {
		this.deadlineOfResolution = deadlineOfResolution;
	}
	
	
	private long createdById;
	private String createdByName;
	private String createdByDesignation;
	private String createdDate;
	private String resourceCategory;
	private String details;
	private String status;
	private String dateOfResolution;
	private String remarks;
	private String priority;
	private String deadlineOfResolution;

	
	
}
